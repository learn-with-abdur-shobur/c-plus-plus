#include <iostream>
using namespace std;
int main (){

    // size of integer 
    int a;
    a=10;
    cout<< "Size of int: "<<sizeof(a)<<endl;

    // size of float 
    float b;
    cout<< "Size of float: "<<sizeof(b)<<endl;

    // size of char 
    char c;
    cout<< "Size of a char: "<<sizeof(c)<<endl;

    // size of bool 
    bool d;
    cout<< "Size of a bool: "<<sizeof(d)<<endl;


    return 0;
}